        /// <summary>
        /// Read the device configuration from the ASCOM Profile store
        /// </summary>
        internal void ReadProfile()
        {
            using (Profile driverProfile = new Profile())
            {
                driverProfile.DeviceType = "Dome";
                tl.Enabled = Convert.ToBoolean(driverProfile.GetValue(s_csDriverID, traceStateProfileName, string.Empty, traceStateDefault));
                comPort = driverProfile.GetValue(s_csDriverID, comPortProfileName, string.Empty, comPortDefault);
            }
        }

        /// <summary>
        /// Write the device configuration to the  ASCOM  Profile store
        /// </summary>
        internal void WriteProfile()
        {
            using (Profile driverProfile = new Profile())
            {
                driverProfile.DeviceType = "Dome";
                driverProfile.WriteValue(s_csDriverID, traceStateProfileName, tl.Enabled.ToString());
                driverProfile.WriteValue(s_csDriverID, comPortProfileName, comPort.ToString());
            }
        }

